// Write a "while" loop that will perform exactly the same repetitive code as this:
console.log(1);
console.log(2);
console.log(3);
console.log(4);
console.log(5);
console.log("Counting completed!");

// Solution using while loop
var i = 1;

while (i < 6) {
  console.log(i);
  i++;
}
console.log("Counting completed!");
